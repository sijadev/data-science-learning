# Basis-Datentypen in Python

# Integer (Ganzzahlen)
alter = 25
anzahl = 100

# Float (Dezimalzahlen)
preis = 19.99
temperatur = -5.5

# String (Text)
name = "Python"
message = 'Hallo Welt!'

# Boolean (Wahrheitswerte)
ist_aktiv = True
ist_fertig = False

# None (Kein Wert)
ergebnis = None

print(f"Alter: {alter} (Typ: {type(alter)})")
print(f"Preis: {preis} (Typ: {type(preis)})")
print(f"Name: {name} (Typ: {type(name)})")
print(f"Aktiv: {ist_aktiv} (Typ: {type(ist_aktiv)})")
